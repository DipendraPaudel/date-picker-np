const UPDATED_NEPALI_MONTHS_COUNT: UpdatedNepaliMonthsCountType[] = [];

export const getStartWeek = () => {
  let lastWeekDay = START_WEEK_DAY_OF_2000;

  for (let i = 0; i < NEPALI_MONTHS_COUNT.length; i++) {
    const currentData = NEPALI_MONTHS_COUNT[i];

    UPDATED_NEPALI_MONTHS_COUNT.push({
      ...currentData,
      startWeek: currentData.months.map((month) => {
        const startWeekDay = lastWeekDay;
        lastWeekDay = (month + startWeekDay) % 7;
        lastWeekDay = lastWeekDay === 0 ? 7 : lastWeekDay;

        return startWeekDay;
      }),
    });
  }
};

getStartWeek();

export { UPDATED_NEPALI_MONTHS_COUNT };
